// ----- Primitive aliases ------------------------------------------------------

/// 32-byte SHA-256 digest. Used internally for content-hash work and
/// as the inner representation of the typed id newtypes below.
///
/// Distinct from a chain account address (`S::Address`, 28 bytes in
/// the canonical SDK config). For protocol-level identifiers (schema
/// ids, attestor-set ids, attestation ids, payload hashes) we use the
/// typed Bech32-encoded newtypes generated by `impl_hash32_type!`.
/// Account fields like [`Schema::owner`] and [`Attestation::submitter`]
/// use `S::Address` directly.
pub type Hash32 = [u8; 32];

// Per #46, every Ligate-minted identifier has a typed Bech32 prefix.
// `impl_hash32_type!` generates a newtype around `[u8; N]` plus
// Display, FromStr, Borsh, Serde, JsonSchema, and bech32m encoding /
// decoding using the supplied prefix. The state layer requires keys
// to be `FromStr + Display`; these macro calls satisfy that.
//
// Each macro invocation generates helper functions
// (`__impl_hash32_type_prefix`, `__bech32_hrp`, `__bech32_conversion_impls`)
// scoped to its module. We put each call in its own private module
// to keep those names from colliding, then re-export the public
// types up to the crate root.

