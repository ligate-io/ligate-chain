/// Event type emitted by the attestation module.
///
/// Reserved for v0; the module's externally interesting state changes
/// are already captured in the `attestations` map and queryable via
/// RPC. We carry the typed enum (rather than `()`) because the SDK's
/// `Module` trait requires `Event: BorshSerialize + BorshDeserialize +
/// JsonSchema + Send + Sync + 'static + PartialEq + Debug`, and to leave
/// space for typed events when the module grows them. Adding new
/// variants here is non-breaking.
#[derive(Clone, Debug, PartialEq, Eq, BorshSerialize, BorshDeserialize, schemars::JsonSchema)]
pub enum AttestationEvent {
    /// Placeholder variant. Never emitted in v0; reserved so the enum
    /// is non-empty (Borsh derives require at least one variant on
    /// enums) without committing to an event shape we'd later
    /// regret. Real events land alongside the query RPC in #21.
    #[doc(hidden)]
    Reserved,
}

impl<S: Spec> Module for AttestationModule<S> {
    type Spec = S;
    type Config = AttestationConfig<S>;
    type CallMessage = CallMessage<S>;
    type Event = AttestationEvent;
    type Error = anyhow::Error;

    fn genesis(
        &mut self,
        _genesis_rollup_header: &<<S as Spec>::Da as DaSpec>::BlockHeader,
        config: &Self::Config,
        state: &mut impl GenesisState<S>,
    ) -> anyhow::Result<()> {
        self.seed_from_config(config, state)
    }

    fn call(
        &mut self,
        msg: Self::CallMessage,
        context: &Context<Self::Spec>,
        state: &mut impl TxState<S>,
    ) -> Result<(), Self::Error> {
        match msg {
            CallMessage::RegisterAttestorSet { members, threshold } => {
                self.handle_register_attestor_set(members, threshold, context, state)
            }
            CallMessage::RegisterSchema {
                name,
                version,
                attestor_set,
                fee_routing_bps,
                fee_routing_addr,
            } => self.handle_register_schema(
                name,
                version,
                attestor_set,
                fee_routing_bps,
                fee_routing_addr,
                context,
                state,
            ),
            CallMessage::SubmitAttestation {
                schema_id,
                payload_hash,
                signatures,
            } => self.handle_submit_attestation(schema_id, payload_hash, signatures, context, state),
        }
    }
}

impl<S: Spec> AttestationModule<S> {
    /// Apply an [`AttestationConfig`] during [`Module::genesis`].
    ///
    /// Stores treasury and fee parameters in state, then registers each
    /// configured attestor set and schema with the same validation rules
    /// the `RegisterAttestorSet` and `RegisterSchema` handlers use at
    /// runtime. Rejects duplicates and orphan / over-cap fee routing.
    ///
    /// Genesis bypasses fee charging by writing directly to state: there
    /// is no transaction sender at genesis to charge from. Fees apply
    /// only to user txs after the chain is live.
    fn seed_from_config(
        &mut self,
        config: &AttestationConfig<S>,
        state: &mut impl GenesisState<S>,
    ) -> anyhow::Result<()> {
        self.treasury.set(&config.treasury, state)?;
        self.lgt_token_id.set(&config.lgt_token_id, state)?;
        self.attestation_fee.set(&config.attestation_fee, state)?;
        self.schema_registration_fee.set(&config.schema_registration_fee, state)?;
        self.attestor_set_fee.set(&config.attestor_set_fee, state)?;

        for initial in &config.initial_attestor_sets {
            if initial.members.is_empty() {
                return Err(AttestationError::EmptyAttestorSet.into());
            }
            if initial.threshold == 0 {
                return Err(AttestationError::ZeroThreshold.into());
            }
            if usize::from(initial.threshold) > initial.members.len() {
                return Err(AttestationError::ThresholdExceedsMembers {
                    threshold: initial.threshold,
                    count: initial.members.len(),
                }
                .into());
            }

            let mut sorted_members = initial.members.clone();
            sorted_members.sort_unstable_by(|a, b| a.as_bytes().cmp(b.as_bytes()));

            let id = AttestorSet::derive_id(&sorted_members, initial.threshold);
            if self.attestor_sets.get(&id, state)?.is_some() {
                return Err(AttestationError::DuplicateAttestorSet.into());
            }
            self.attestor_sets.set(
                &id,
                &AttestorSet { members: sorted_members, threshold: initial.threshold },
                state,
            )?;
        }

        for schema in &config.initial_schemas {
            if schema.fee_routing_bps > MAX_BUILDER_BPS {
                return Err(AttestationError::FeeRoutingExceedsCap {
                    bps: schema.fee_routing_bps,
                    cap: MAX_BUILDER_BPS,
                }
                .into());
            }
            if (schema.fee_routing_bps > 0) != schema.fee_routing_addr.is_some() {
                return Err(AttestationError::OrphanFeeRouting.into());
            }
            if self.attestor_sets.get(&schema.attestor_set, state)?.is_none() {
                return Err(AttestationError::UnknownAttestorSet.into());
            }

            let id = Schema::<S>::derive_id(&schema.owner, &schema.name, schema.version);
            if self.schemas.get(&id, state)?.is_some() {
                return Err(AttestationError::DuplicateSchema.into());
            }
            self.schemas.set(
                &id,
                &Schema {
                    owner: schema.owner.clone(),
                    name: schema.name.clone(),
                    version: schema.version,
                    attestor_set: schema.attestor_set,
                    fee_routing_bps: schema.fee_routing_bps,
                    fee_routing_addr: schema.fee_routing_addr.clone(),
                },
                state,
            )?;
        }

        Ok(())
    }

    /// Handle [`CallMessage::RegisterAttestorSet`].
    fn handle_register_attestor_set(
        &mut self,
        members: Vec<PubKey>,
        threshold: u8,
        context: &Context<S>,
        state: &mut impl TxState<S>,
    ) -> anyhow::Result<()> {
        if members.is_empty() {
            return Err(AttestationError::EmptyAttestorSet.into());
        }
        if threshold == 0 {
            return Err(AttestationError::ZeroThreshold.into());
        }
        if usize::from(threshold) > members.len() {
            return Err(AttestationError::ThresholdExceedsMembers {
                threshold,
                count: members.len(),
            }
            .into());
        }

        // Store members in sorted order so later reads match the id derivation.
        let mut sorted_members = members;
        sorted_members.sort_unstable_by(|a, b| a.as_bytes().cmp(b.as_bytes()));

        let id = AttestorSet::derive_id(&sorted_members, threshold);
        if self.attestor_sets.get(&id, state)?.is_some() {
            return Err(AttestationError::DuplicateAttestorSet.into());
        }

        // Charge the registration fee after validation and duplicate
        // check, before state mutation. If the submitter has insufficient
        // `$LGT`, the bank transfer returns `Err`, the tx state unwinds,
        // and the whole tx fails atomically.
        let fee = self.attestor_set_fee.get(state)?.unwrap_or(DEFAULT_ATTESTOR_SET_FEE_LGT_NANOS);
        self.charge_to_treasury(context.sender(), fee, state)?;

        self.attestor_sets.set(
            &id,
            &AttestorSet { members: sorted_members, threshold },
            state,
        )?;

        Ok(())
    }

    /// Handle [`CallMessage::RegisterSchema`].
    #[allow(clippy::too_many_arguments)]
    fn handle_register_schema(
        &mut self,
        name: String,
        version: u32,
        attestor_set: AttestorSetId,
        fee_routing_bps: u16,
        fee_routing_addr: Option<S::Address>,
        context: &Context<S>,
        state: &mut impl TxState<S>,
    ) -> anyhow::Result<()> {
        if fee_routing_bps > MAX_BUILDER_BPS {
            return Err(AttestationError::FeeRoutingExceedsCap {
                bps: fee_routing_bps,
                cap: MAX_BUILDER_BPS,
            }
            .into());
        }
        if (fee_routing_bps > 0) != fee_routing_addr.is_some() {
            return Err(AttestationError::OrphanFeeRouting.into());
        }
        if self.attestor_sets.get(&attestor_set, state)?.is_none() {
            return Err(AttestationError::UnknownAttestorSet.into());
        }

        let owner: S::Address = context.sender().clone();
        let id = Schema::<S>::derive_id(&owner, &name, version);

        if self.schemas.get(&id, state)?.is_some() {
            return Err(AttestationError::DuplicateSchema.into());
        }

        // Charge the registration fee after validation and duplicate
        // check, before state mutation. Insufficient balance â†’ `Err`,
        // tx state unwinds, no state changes.
        let fee = self
            .schema_registration_fee
            .get(state)?
            .unwrap_or(DEFAULT_SCHEMA_REGISTRATION_FEE_LGT_NANOS);
        self.charge_to_treasury(context.sender(), fee, state)?;

        self.schemas.set(
            &id,
            &Schema { owner, name, version, attestor_set, fee_routing_bps, fee_routing_addr },
            state,
        )?;

        Ok(())
    }

    /// Handle [`CallMessage::SubmitAttestation`].
    ///
    /// Enforces:
    /// 1. Schema exists.
    /// 2. `(schema_id, payload_hash)` has not been written before.
    /// 3. Every signature is valid ed25519 over the canonical
    ///    [`SignedAttestationPayload`] digest.
    /// 4. Every signing pubkey is a member of the schema's [`AttestorSet`].
    /// 5. No pubkey appears twice in the submission.
    /// 6. Count of valid signatures is at least the attestor set's
    ///    `threshold`.
    ///
    /// Fees are charged BEFORE the attestation is written; failure of
    /// either the treasury or builder transfer aborts the tx via the
    /// SDK's TxState unwind.
    fn handle_submit_attestation(
        &mut self,
        schema_id: SchemaId,
        payload_hash: PayloadHash,
        signatures: Vec<AttestorSignature>,
        context: &Context<S>,
        state: &mut impl TxState<S>,
    ) -> anyhow::Result<()> {
        let schema = self
            .schemas
            .get(&schema_id, state)?
            .ok_or(AttestationError::UnknownSchema)?;

        let attestation_id = AttestationId::from_pair(&schema_id, &payload_hash);
        if self.attestations.get(&attestation_id, state)?.is_some() {
            return Err(AttestationError::DuplicateAttestation.into());
        }

        let attestor_set = self
            .attestor_sets
            .get(&schema.attestor_set, state)?
            .ok_or(AttestationError::MissingAttestorSet)?;

        let submitter: S::Address = context.sender().clone();
        // TODO: pull the real block timestamp once the runtime exposes
        // a slot/header view to handlers. For now, attestations carry
        // `timestamp = 0`; the write-once key prevents replay either way.
        let timestamp = 0u64;

        // Canonical digest attestors sign over. Must be computed after
        // we know `submitter` and `timestamp`; those are part of what's
        // signed.
        let digest = SignedAttestationPayload::<S> {
            schema_id,
            payload_hash,
            submitter: submitter.clone(),
            timestamp,
        }
        .digest();

        validate_signatures(&signatures, &attestor_set, &digest)?;

        // Charge the attestation fee BEFORE writing the attestation.
        // Split per `schema.fee_routing_bps`: builder share goes to
        // `schema.fee_routing_addr` (if configured), remainder to the
        // treasury. Two transfers (treasury then builder) so a partial
        // failure unwinds both via the SDK's TxState.
        let total_fee = self
            .attestation_fee
            .get(state)?
            .unwrap_or(DEFAULT_ATTESTATION_FEE_LGT_NANOS);
        let (builder_share, treasury_share) =
            split_attestation_fee(total_fee, schema.fee_routing_bps);
        self.charge_to_treasury(context.sender(), treasury_share, state)?;
        if let Some(addr) = &schema.fee_routing_addr {
            if builder_share != Amount::ZERO {
                self.charge_to_builder(context.sender(), addr, builder_share, state)?;
            }
        }

        self.attestations.set(
            &attestation_id,
            &Attestation { schema_id, payload_hash, submitter, timestamp, signatures },
            state,
        )?;

        Ok(())
    }

    /// Move `amount` `$LGT` nanos from `submitter` to the treasury via
    /// `sov-bank`, then bump the cumulative
    /// [`AttestationModule::total_treasury_collected`] counter.
    ///
    /// Returns `Ok(())` for `amount == 0` without touching the bank or
    /// counter, so callers don't need to special-case zero-fee paths.
    /// Bumps the counter only after a successful transfer; the SDK's
    /// `TxState` unwind handles the failure case (no half-state).
    fn charge_to_treasury(
        &mut self,
        submitter: &S::Address,
        amount: Amount,
        state: &mut impl TxState<S>,
    ) -> anyhow::Result<()> {
        if amount == Amount::ZERO {
            return Ok(());
        }
        let lgt_token_id =
            self.lgt_token_id.get(state)?.ok_or(AttestationError::ChainNotConfigured)?;
        let treasury =
            self.treasury.get(state)?.ok_or(AttestationError::ChainNotConfigured)?;
        self.bank.transfer_from(
            submitter,
            &treasury,
            Coins { amount, token_id: lgt_token_id },
            state,
        )?;
        self.add_to_treasury(amount, state)?;
        Ok(())
    }

    /// Move `amount` `$LGT` nanos from `submitter` to `builder_addr` via
    /// `sov-bank`, then bump the cumulative
    /// [`AttestationModule::builder_fees_collected`] counter for that
    /// address. Same `amount == 0` short-circuit and same atomicity
    /// guarantees as [`Self::charge_to_treasury`].
    fn charge_to_builder(
        &mut self,
        submitter: &S::Address,
        builder_addr: &S::Address,
        amount: Amount,
        state: &mut impl TxState<S>,
    ) -> anyhow::Result<()> {
        if amount == Amount::ZERO {
            return Ok(());
        }
        let lgt_token_id =
            self.lgt_token_id.get(state)?.ok_or(AttestationError::ChainNotConfigured)?;
        self.bank.transfer_from(
            submitter,
            builder_addr,
            Coins { amount, token_id: lgt_token_id },
            state,
        )?;
        self.add_to_builder(builder_addr, amount, state)?;
        Ok(())
    }

    /// Increment [`AttestationModule::total_treasury_collected`] by `amount`,
    /// saturating at `Amount::MAX` (i.e. `u128::MAX`).
    fn add_to_treasury(
        &mut self,
        amount: Amount,
        state: &mut impl TxState<S>,
    ) -> anyhow::Result<()> {
        let current = self.total_treasury_collected.get(state)?.unwrap_or(Amount::new(0));
        self.total_treasury_collected.set(&current.saturating_add(amount), state)?;
        Ok(())
    }

    /// Increment [`AttestationModule::builder_fees_collected`] for `addr` by
    /// `amount`, saturating at `Amount::MAX`.
    fn add_to_builder(
        &mut self,
        addr: &S::Address,
        amount: Amount,
        state: &mut impl TxState<S>,
    ) -> anyhow::Result<()> {
        let current =
            self.builder_fees_collected.get(addr, state)?.unwrap_or(Amount::new(0));
        self.builder_fees_collected
            .set(addr, &current.saturating_add(amount), state)?;
        Ok(())
    }
}

/// Split an attestation fee into `(builder_share, treasury_share)` per
/// `fee_routing_bps` (basis points, capped at [`MAX_BUILDER_BPS`]).
///
/// `Amount` is a `u128` newtype. The intermediate split is computed as
/// `total / 10_000 * bps + (total % 10_000) * bps / 10_000` to keep the
/// multiplication well below `u128::MAX` even when `total` is itself
/// near the max.
fn split_attestation_fee(total: Amount, bps: u16) -> (Amount, Amount) {
    let total_u128 = total.0;
    let bps_u128 = u128::from(bps);
    let builder_u128 = total_u128 / 10_000 * bps_u128 + (total_u128 % 10_000) * bps_u128 / 10_000;
    let builder = Amount::new(builder_u128);
    let treasury = Amount::new(total_u128.saturating_sub(builder_u128));
    (builder, treasury)
}

/// Validate a submission's [`AttestorSignature`] vector against an
/// [`AttestorSet`] and the canonical attestation digest.
///
/// Enforces, in order:
/// 1. No duplicate pubkey within the submission.
/// 2. Each pubkey is a member of `set.members`.
/// 3. Each pubkey bytes decode as a valid ed25519 point.
/// 4. Each signature's raw bytes are 64 bytes long.
/// 5. The ed25519 signature verifies against `digest` for its pubkey.
/// 6. The count of valid signatures is at least `set.threshold`.
///
/// Ed25519 is the only scheme accepted in v0. BLS aggregates or other
/// schemes would be added as separate variants later; `AttestorSignature.sig`
/// is `Vec<u8>` today precisely to leave room for that.
fn validate_signatures(
    signatures: &[AttestorSignature],
    set: &AttestorSet,
    digest: &Hash32,
) -> anyhow::Result<()> {
    use std::collections::HashSet;

    use ed25519_dalek::{Signature, Verifier, VerifyingKey};

    let members: HashSet<PubKey> = set.members.iter().copied().collect();
    let mut seen: HashSet<PubKey> = HashSet::new();
    let mut valid: u16 = 0;

    for attested in signatures {
        if !seen.insert(attested.pubkey) {
            return Err(AttestationError::DuplicateSignaturePubkey.into());
        }

        if !members.contains(&attested.pubkey) {
            return Err(AttestationError::UnknownAttestorPubkey.into());
        }

        let verifying_key = VerifyingKey::from_bytes(attested.pubkey.as_bytes())
            .map_err(|_| AttestationError::InvalidAttestorPubkey)?;

        let sig_bytes: [u8; 64] = attested
            .sig
            .as_slice()
            .try_into()
            .map_err(|_| AttestationError::BadSignatureLength { actual: attested.sig.len() })?;
        let signature = Signature::from_bytes(&sig_bytes);

        if verifying_key.verify(digest, &signature).is_err() {
            return Err(AttestationError::InvalidSignature.into());
        }

        valid = valid.saturating_add(1);
    }

    let valid_u8: u8 = valid.min(u16::from(u8::MAX)) as u8;
    if valid_u8 < set.threshold {
        return Err(AttestationError::BelowThreshold {
            provided: valid_u8,
            required: set.threshold,
        }
        .into());
    }

    Ok(())
}

